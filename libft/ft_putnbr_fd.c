#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nb[12];
	int		i;

	i = 0;
	if (n == -2147483648)
	{
		nb[i++] = '8';
		n = n / 10;
	}
	if (n == 0)
		write(fd, "0", 1);
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	while (n != 0)
	{
		nb[i++] = (n % 10) + '0';
		n = n / 10;
	}
	while (i > 0)
		write(fd, &nb[i-- - 1], 1);
}
